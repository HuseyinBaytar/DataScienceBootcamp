#CRM analitiği çalışmaları müşterisi olan hemen hemen her kurumda vardır. tek başına CRM analitiğinde iyi olmak bir çok veri analitiği kapsamındaki pozisyonun önünü rahat şekilde açacaktır.



#                           KPI's  ( key performance indicators)
#                                  ( Temel Performans Göstergeleri)

#işe ilk girenlerin merak ettiği, konuyu bilmeyen yeni mezunların öğrenmesi gereken kavramdır.

#şirket,departman ya da çalışanların performanslarını değerlendirmek için kullanılan matematiksel göstergelerdir.

#Customer Acquisition Rate( Müşteri kazanma oranı) // belirli bir zamanda kazanılan müşteri yüzdesidir.

#Customer Retention Rate( Müşteri elde tutma oranı) // şirket ile ilişkisi olan, alışveriş yapmış bir müşterinin, 2. alışverişi yapması veya bizi genel olarak tercih eder mi?

#Customer Churn Rate ( Müşteri Terk Oranı) // varolan müşterilerimizin şirketi terketmesi

#Conversion Rate (Dönüşüm Oranı) // ilan sitesinde, ilanımız var, bin kişi gördü, 10 kişi tıkladıysa dönüşüm oranı 10/1000'dir.

#Growth Rate(Büyüme Oranı) // yılın en başında veya belirli zaman periodlarında şirketlerin koyduğu hedeflerdir.


